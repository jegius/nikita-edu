import template from "./best-in-auto-component.template.js";

export class BestInAutoComponent extends HTMLElement {
    static get name() {
        return "best-in-auto-component";
    }

    constructor() {
        super();
        this.attachShadow({ mode: "open" });
    }

    connectedCallback() {
        this.#render();
    }

    #render() {
        const templateElem = document.createElement("template");
        templateElem.innerHTML = template;
        this.shadowRoot.appendChild(templateElem.content.cloneNode(true));
    }
}
